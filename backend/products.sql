    id          int NOT NULL AUTO_INCREMENT PRIMARY KEY,
    prod_name   varchar(30),
    prod_desc   varchar(4000),
    price       float,
    category    varchar(30),
    source      varchar(30),
    organic     boolean DEFAULT false,
    prod_url    varchar(500)
);

